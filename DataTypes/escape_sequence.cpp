#include <iostream>
#include <string>

// if we want to use a string data type we need to add string header
int main()
{
    std::cout << "hello\tworld"; // \t - enter a tab character
    // \n - new line
    // \v - vertical tab
    // \0 - denotes the end of string
    // \" - indicates a quote
    // \\ - inform the compiler to print one backslash
    return 0;
}